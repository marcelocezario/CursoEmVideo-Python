'''
Desafios aula 7
Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.
'''
numero = int(input('Digite um número: '))
print('O dobro do número é {}'.format(numero*2))
print('O triplo do número é {}'.format(numero*3))
print('A raiz quadrada do número é {}'.format(numero**(1/2)))
